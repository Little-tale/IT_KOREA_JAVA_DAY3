package Day_03;

public class EscapeCharacter
{
	public static void main(String[] args)
	{
		// 제어문자
		System.out.println("안녕? \n 반갑다. \"응..... 그래 \'ㅗ\' \" ");
		// \t활용
		System.out.println("반갑\t습니다.");
		System.out.println("\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\");
		System.out.println("난 \"김재형\" 이다. \'김\',\'재\',\'형\' ");
		// 이게 뭐다?? 이스케이프 문자 다.
		
		
	}

}
